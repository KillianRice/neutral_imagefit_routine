function plotTempWithOffset(analyVar,avgDataset)
% Plot data with offset for clarity

%% Initialize Variables
a            = length(avgDataset);
[~,orderVec] = sort(analyVar.uniqScanList); orderVec = flipud(orderVec);
tempFig      = figure;
offset       = 50;
[dataHanNum,legStr] = deal(cell(1,a));
xRange       = [-2 2];

% Plotting variables
COLORS     = {'k' 'r' 'b' 'm' 'g' [0 0.75 0.75] [0.6 0.2 1] [1 0.588 0] [0.5 0.5 0.5]};
MARKER     = {'o' 's' 'p' '>' 'd' '*' '<' 'h' '^'};

%% Plotting
for j = 1:a
    i = orderVec(j);
    % Rename data into more concise variables
    ind      = analyVar.funcDataScale(avgDataset{i}.simScanIndVar);
    xTemp    = avgDataset{i}.avgAtomTempX; yTemp    = avgDataset{i}.avgAtomTempY;
    T0x      = mean(xTemp(1:5));           T0y      = mean(yTemp(1:5)); 
    
    legStr{j} = [num2str(analyVar.uniqScanList(i)) ' us'];
    
    % Pick value of color and marker to use
    indIter = mod(j,length(COLORS)); if indIter == 0; indIter = length(COLORS); end
    
    % Data Curves
    figure(tempFig); subplot(1,2,1);
    vertPos = (a - j - 1)*offset;
    dataHanNum{j} = plot(ind,xTemp + vertPos); hold on;
    line(xRange,[1 1].*(T0x + vertPos),...
        'Color'     ,   COLORS{indIter} ,...
        'LineStyle' ,   '--'            ,...
        'LineWidth',    2)
    set(dataHanNum{j}(1),...
        'Color'             ,   COLORS{indIter} ,...
        'LineStyle'         ,   'none'          ,...
        'Marker'            ,   MARKER{indIter} ,...
        'MarkerSize'        ,   10              ,...
        'MarkerFaceColor'   ,   COLORS{indIter} )
    
    % Data Curves
    subplot(1,2,2);
    vertPos = (a - j - 1)*offset;
    dataHanNum{j} = plot(ind,yTemp + vertPos); hold on;
    line(xRange,[1 1].*(T0y + vertPos),...
        'Color'     ,   COLORS{indIter} ,...
        'LineStyle' ,   '--'            ,...
        'LineWidth',    2)
    set(dataHanNum{j}(1),...
        'Color'             ,   COLORS{indIter} ,...
        'LineStyle'         ,   'none'          ,...
        'Marker'            ,   MARKER{indIter} ,...
        'MarkerSize'        ,   10              ,...
        'MarkerFaceColor'   ,   COLORS{indIter} )
end

subplot(1,2,1);
grid on
set(gca,...
    'FontSize'      ,   35              ,...
    'FontWeight'    ,   'Bold'          ,...
    'YTickLabel'    ,   []              ,...
    'LineWidth'     ,   2               )
xlabel('^3P_1 Detuning [MHz]')
ylabel('X Temperature')
legHan = legend(cellfun(@(x) x(1),dataHanNum),legStr,...
    'location'  ,   'NorthWest'  );
set(legHan,...
    'FontSize'  ,   16  )
axis tight
xlim(xRange)

subplot(1,2,2);
grid on
set(gca,...
    'FontSize'      ,   35              ,...
    'FontWeight'    ,   'Bold'          ,...
    'YTickLabel'    ,   []              ,...
    'LineWidth'     ,   2               )
xlabel('^3P_1 Detuning [MHz]')
ylabel('Y Temperature')
legHan = legend(cellfun(@(x) x(1),dataHanNum),legStr,...
    'location'  ,   'NorthWest'  );
set(legHan,...
    'FontSize'  ,   16  )
axis tight
xlim(xRange)
end